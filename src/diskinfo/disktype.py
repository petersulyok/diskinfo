class DiskType:
    """Constant values for disk types and for their names."""
    HDD = 1
    """Hard disk type."""
    SSD = 2
    """SSD disk type."""
    NVME = 4
    """NVME disk type."""
    HDD_STR = "HDD"
    """Hard disk type name."""
    SSD_STR = "SSD"
    """SSD disk type name."""
    NVME_STR = "NVME"
    """NVME disk type name."""

# End
